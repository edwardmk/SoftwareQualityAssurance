package simpleCodingConventions;

public class IfWhileForStatments {

	public static void main ( String[] args) {
		int num = 1;
		
		//If Statement
		
		if ( num == 1) {
			//code
		} 
		else {
			//code
		}
		
		//For Statement
		
		for (int i = 0; i < 5; i++ ) {
			//code
		}
		
		//While Statement
		
		while ( num < 5 ) {
			//code
		}
	}
	
}